import UIKit

extension UIFont {
    
    static func helveticaNeueBold30() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Bold", size: 30)
    }
    
    static func helveticaNeueLight24() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Light", size: 24)
    }
    
    static func helveticaNeueBold20() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Bold", size: 20)
    }
}
